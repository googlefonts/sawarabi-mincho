package jp.sourceforge.sawarabifonts.chartool;

public class CharProperty {
	final String character;
	final String name;
	final String code;

	public CharProperty(String character, String code) {
		this(character, code, "");
	}

	public CharProperty(String character, String code, String name) {
		this.character = character;
		this.code = code;
		this.name = name;
	}
}
